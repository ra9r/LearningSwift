import SwiftUI


extension Color {
    static let theme = Theme()
}

struct Theme {
    let accentColor = Color("AccentColor")
    let backgroundColor = Color("BackgroundColor")
    let buttonColor = Color("ButtonColor")
    let buttonTextColor = Color("ButtonTextColor")
    let headlineColor = Color("HeadlineColor")
    let highlightColor = Color("HighlightColor")
    let mainColor = Color("MainColor")
    let paragraphColor = Color("ParagraphColor")
    let secondaryColor = Color("SecondaryColor")
    let strokeColor = Color("StrokeColor")
    let tertiaryColor = Color("TertiaryColor")
}


